package com.example.slip13q2;

public class Details {
    String id;
    String pwd;

public Details(String id, String pwd){
    this.id=id;
    this.pwd=pwd;
}
public String getId(){
    return this.id;
}
public void setId(String id){
    this.id=id;
}
public String getPwd(){
    return this.pwd;
}
public void setPwd(String pwd){
    this.pwd=pwd;
}
}
